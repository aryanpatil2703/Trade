import express from 'express';
import cors from 'cors';
import helmet from 'helmet';
import dotenv from 'dotenv';
import path from 'path';
import fs from 'fs';

// Import routes
import lighthouseRoutes from './routes/lighthouse';
import mcpAdapterRoutes from './routes/mcp-adapter';
import mintRoutes from './routes/mint';

dotenv.config();

const app = express();
const PORT = process.env.PORT || 3001;

// Middleware
app.use(helmet());
app.use(cors());
app.use(express.json({ limit: '50mb' }));
app.use(express.urlencoded({ extended: true }));

// Create logs directory if it doesn't exist
const logsDir = path.join(__dirname, '../logs');
if (!fs.existsSync(logsDir)) {
  fs.mkdirSync(logsDir, { recursive: true });
}

// Routes
app.use('/api/lighthouse', lighthouseRoutes);
app.use('/api/mcp', mcpAdapterRoutes);
app.use('/api/mint', mintRoutes);

// Health check endpoint
app.get('/health', (req, res) => {
  res.json({ 
    status: 'healthy', 
    timestamp: new Date().toISOString(),
    services: {
      lighthouse: !!process.env.LIGHTHOUSE_API_KEY,
      blockscout: !!process.env.BLOCKSCOUT_MCP_URL,
      mumbai: !!process.env.RPC_URL_MUMBAI
    }
  });
});

// Error handling middleware
app.use((err: any, req: express.Request, res: express.Response, next: express.NextFunction) => {
  console.error('Error:', err);
  res.status(500).json({ 
    error: 'Internal server error',
    message: process.env.NODE_ENV === 'development' ? err.message : 'Something went wrong'
  });
});

// 404 handler
app.use('*', (req, res) => {
  res.status(404).json({ error: 'Route not found' });
});

app.listen(PORT, () => {
  console.log(`ðŸš€ Backend server running on port ${PORT}`);
  console.log(`ðŸ“Š Health check: http://localhost:${PORT}/health`);
  console.log(`ðŸ”— Lighthouse API: http://localhost:${PORT}/api/lighthouse`);
  console.log(`ðŸ¤– MCP Adapter: http://localhost:${PORT}/api/mcp`);
});
